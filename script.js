// 1. Write the logic to get computer choice
function getComputerChoice() {
    let compChoice = Math.floor(Math.random() * (3 - 1 + 1) + 1)
    switch(compChoice) {
        case 1 :
            return 'rock'
            break
        case 2 :
            return 'paper'
            break
        case 3 :
            return 'scissor'
    }
}


